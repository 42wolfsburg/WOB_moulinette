#include <unistd.h>

void	ft_quizzbug(void)
{
	int i = 1;

	while (i <= 100)
	{
		// printf("%d\n", i);
		if (i % 21 == 0)
			write (1, "quizzbug\n", 9);
		else if (i % 3 == 0)
			write (1, "quizz\n", 6);
		else if (i % 7 == 0)
			write (1, "bug\n", 4);
		i++;
	}
}
